import React from "react";
import { OrderPage } from "../modules/order/page";

const Order = () => {
  return <OrderPage />;
};

export const getServerSideProps = async ({
  req,
  query,
}: {
  req: any;
  query: any;
}) => {
  if (!req?.cookies.user_id) {
    return {
      redirect: {
        destination: "/auth/login",
        permenant: false,
      },
    };
  }
  return {
    props: {},
  };
};

export default Order;
